/**
 * Provider module emission (RFC-0001 §2, Track D · D2).
 *
 * Emits one fully codegen-owned NestJS module per `definitions/providers/*.yaml`:
 *
 *     src/integrations/providers/<slug>/<slug>.provider.module.ts
 *
 * The module imports the provider's declared `auth.strategy` and `client.class`
 * (by their `import-path#Export` refs) and provides + exports them under
 * provider-specific DI tokens `<SLUG>_AUTH_STRATEGY` and `<SLUG>_CLIENT`.
 *
 * Token shape follows the auth subsystem contract
 * (runtime/subsystems/auth/auth.tokens.ts): provider strategies are registered
 * under provider-specific tokens — the subsystem deliberately does NOT mandate
 * a single `AUTH_STRATEGY` token because an app runs many concurrent
 * strategies, one per provider. Aggregation into the auth `STRATEGY_REGISTRY`
 * map and the per-surface adapter/entity-source registry (RFC-0001 §3) is a
 * later-step concern (D3/D4); this module emits only the provider's own
 * strategy + client wiring.
 *
 * Fully codegen-owned: carries the `@generated` DO-NOT-EDIT banner and is
 * re-emitted byte-identically on every run from an unchanged YAML tree
 * (deterministic content, slug-sorted discovery — RFC-0001 §2 idempotency
 * contract).
 *
 * This module is a direct string-builder (mirroring barrel-generator.ts), not
 * a Hygen template: the output is a single fixed-shape file with no author
 * seam, so the byte-identical re-emit contract is easiest to honour and test
 * as a pure function.
 */

import { existsSync, mkdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { dirname, isAbsolute, join, resolve } from "node:path";
import type { AnalysisIssue } from "../../analyzer/types";
