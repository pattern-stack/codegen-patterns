// ============================================================================
// Naming
// ============================================================================

/** `hubspot-crm` → `HubspotCrm`. Slug is already kebab-lower (schema-enforced). */
export function providerPascalCase(slug: string): string {
  return slug
    .split("-")
    .filter(Boolean)
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join("");
}

/** `hubspot-crm` → `HUBSPOT_CRM`. */
export function providerConstantCase(slug: string): string {
  return slug.replace(/-/g, "_").toUpperCase();
}

// ============================================================================
// Banner
// ============================================================================

/**
 * The `@generated` DO-NOT-EDIT banner, carrying the canonical marker
 * (`@generated by @pattern-stack/codegen`) so the repo-wide banner grep/contract
 * holds. A local constant rather than the Hygen `renderGeneratedBanner` helper,
 * matching the barrel-generator string-builder precedent (the shared helper is
 * for `.ejs.t` templates).
 */
function providerModuleBanner(sourceYaml: string): string {
  return (
    `// @generated by @pattern-stack/codegen from ${sourceYaml} — DO NOT EDIT.\n` +
    `// Hand edits are overwritten on re-emit. Regenerate with \`bun run codegen\`. ` +
    `To change this provider, edit its \`definitions/providers/*.yaml\`.`
  );
}

// ============================================================================
// Pure emit
// ============================================================================

/**
 * Render the `<slug>.provider.module.ts` content for one provider. Pure —
 * deterministic in its inputs, so two calls with the same definition produce
 * byte-identical output.
 *
 * @param def        Validated provider definition.
 * @param sourceYaml Source path interpolated into the banner (e.g.
 *                   `definitions/providers/google.yaml`). Informational only.
 */
export function generateProviderModule(
  def: ProviderDefinition,
  sourceYaml: string,
): string {
  const { slug } = def;
  const Pascal = providerPascalCase(slug);
  const CONST = providerConstantCase(slug);
  const strategy = parseImportRef(def.auth.strategy);
  const client = parseImportRef(def.client.class);

  const strategyToken = `${CONST}_AUTH_STRATEGY`;
  const clientToken = `${CONST}_CLIENT`;
  const surfaces = def.surfaces.join(", ");
  const title = def.display_name ? `${def.display_name} (\`${slug}\`)` : `\`${slug}\``;

  // RFC-0003 R5: a provider whose surfaces are ALL read-primitive (calendar/
  // mail/transcript/messaging) uses per-connection auth — its adapters build a
  // per-connection client inside enumerate/hydrate from
  // `ctx.subscription.externalRef`, so there is no provider-level singleton
  // client. For these, emit a CLIENT-LESS, registry-backed module: the strategy
  // is constructed eagerly by the consumer's @Global STRATEGY_REGISTRY (with its
  // real connectionReader/encryption deps) and merely LOOKED UP here. Providers
  // with any non-read-primitive surface (e.g. crm, provider-level single-account
  // auth) keep the legacy client-ful, bare-class shape.
  if (isClientlessProvider(def.surfaces)) {
    return `${providerModuleBanner(sourceYaml)}
/**
 * Provider module for ${title}.
 *
 * Surfaces: ${surfaces}.
 *
 * Per-connection (read-primitive) provider — exposes \`${strategyToken}\`, the
 * per-connection credential resolver the interaction adapters use (RFC-0003 R5:
 * \`auth.resolve(ctx.subscription.externalRef)\`). There is NO provider-level
 * singleton client; adapters build a per-connection client inside their
 * \`enumerate\`/\`hydrate\`. \`${strategyToken}\` resolves the already-built
 * \`${slug}\` strategy from the @Global \`STRATEGY_REGISTRY\` (wired by the
 * consumer's AuthBindings module) — the strategy may need DI deps a bare class
 * can't satisfy, so it is registry-resolved, not provided directly.
 */
import { Module } from '@nestjs/common';
import {
  type ProviderStrategyRegistry,
  STRATEGY_REGISTRY,
} from '@pattern-stack/codegen/subsystems';

/** DI token for the \`${slug}\` provider's auth strategy (resolved from STRATEGY_REGISTRY). */
export const ${strategyToken} = Symbol('${strategyToken}');

const PROVIDER_SLUG = '${slug}';

@Module({
  providers: [
    {
      provide: ${strategyToken},
      useFactory: (registry: ProviderStrategyRegistry) => {
        const strategy = registry.get(PROVIDER_SLUG);
        if (!strategy) {
          throw new Error(
            \`${Pascal}ProviderModule: no '\${PROVIDER_SLUG}' strategy registered in STRATEGY_REGISTRY (wire it in your consumer AuthBindings module)\`,
          );
        }
        return strategy;
      },
      inject: [STRATEGY_REGISTRY],
    },
  ],
  exports: [${strategyToken}],
})
export class ${Pascal}ProviderModule {}
`;
  }

  // Client-ful (legacy) shape — provider-level single-account auth (e.g. crm).
  // Strategy and client may be exported from the same module path; emit one
  // import statement per ref regardless — TypeScript collapses duplicates and
  // keeping them separate matches the 1:1 YAML→import reading.
  return `${providerModuleBanner(sourceYaml)}
/**
 * Provider module for ${title}.
 *
 * Surfaces: ${surfaces}.
 *
 * Wires the declared auth strategy and API client under the provider-specific
 * DI tokens \`${strategyToken}\` and \`${clientToken}\`. Per the auth subsystem
 * contract, strategies are registered under provider-specific tokens rather
 * than a single \`AUTH_STRATEGY\`. Registry aggregation (RFC-0001 §3) is
 * emitted by a later codegen step.
 */
import { Module } from '@nestjs/common';
import { ${strategy.exportName} } from '${strategy.path}';
import { ${client.exportName} } from '${client.path}';

/** DI token for the \`${slug}\` provider's auth strategy. */
export const ${strategyToken} = Symbol('${strategyToken}');
/** DI token for the \`${slug}\` provider's API client. */
export const ${clientToken} = Symbol('${clientToken}');

@Module({
  providers: [
    ${strategy.exportName},
    ${client.exportName},
    { provide: ${strategyToken}, useExisting: ${strategy.exportName} },
    { provide: ${clientToken}, useExisting: ${client.exportName} },
  ],
  exports: [
    ${strategy.exportName},
    ${client.exportName},
    ${strategyToken},
    ${clientToken},
  ],
})
export class ${Pascal}ProviderModule {}
`;
}

// ============================================================================
// tsconfig path-alias resolution (for the pre-flight import check)
// ============================================================================

/**
 * Resolve the consumer source root + path-alias map from a `tsconfig.json` so
 * the D1 import pre-flight (`validateProviders`) can resolve `@app/…#Export`
 * refs against real files.
 *
 * Reads `compilerOptions.baseUrl` (default `.`) and `compilerOptions.paths`,
 * mapping each `"@app/*": ["./src/*"]` entry to `@app` → `<root>/<baseUrl>/src`
 * (the first target wins; the `/*` glob suffix is stripped). Returns `null` if
 * the file is absent or unparseable — the caller then skips the import check.
 */
export function resolveTsconfigAliases(
  consumerRoot: string,
): { sourceRoot: string; aliases: Record<string, string> } | null {
  const tsconfigPath = join(consumerRoot, "tsconfig.json");
  if (!existsSync(tsconfigPath)) return null;

  let parsed: { compilerOptions?: { baseUrl?: string; paths?: Record<string, string[]> } };
  try {
    parsed = JSON.parse(stripJsonComments(readFileSync(tsconfigPath, "utf-8")));
  } catch {
    return null;
  }

  const compilerOptions = parsed.compilerOptions ?? {};
  const baseUrl = compilerOptions.baseUrl ?? ".";
  const sourceRoot = isAbsolute(baseUrl)
    ? baseUrl
    : resolve(consumerRoot, baseUrl);

  const aliases: Record<string, string> = {};
  for (const [pattern, targets] of Object.entries(compilerOptions.paths ?? {})) {
    if (!Array.isArray(targets) || targets.length === 0) continue;
    const aliasKey = pattern.replace(/\/\*$/, "");
    const target = targets[0].replace(/\/\*$/, "");
    aliases[aliasKey] = isAbsolute(target) ? target : resolve(sourceRoot, target);
  }

  return { sourceRoot, aliases };
}

/** Strip `//` and `/* *​/` comments so JSON.parse accepts tsconfig with comments. */
function stripJsonComments(input: string): string {
  // Remove block comments, then line comments. Conservative: does not attempt
  // to preserve comment-like substrings inside strings, which tsconfig paths
  // do not contain in practice.
  return input
    .replace(/\/\*[\s\S]*?\*\//g, "")
    .replace(/(^|[^:])\/\/.*$/gm, "$1");
}

// ============================================================================
// Orchestration: discover → load → validate → write
// ============================================================================

export interface GenerateProviderModulesOptions {
  /** `definitions/providers/` directory. Absent/empty → no-op skip. */
  providersDir: string;
  /** Output root, typically `<backend_src>/integrations/providers`. */
  outputRoot: string;
  /**
   * The closed set of entity surfaces (for the surface cross-check). When not
   * supplied, pass an empty set — every provider will then fail the surface
   * check, which is the correct signal that no entity declares a surface.
   */
  entitySurfaces: Set<string>;
  /** Consumer source root for the import pre-flight (from tsconfig baseUrl). */
  sourceRoot?: string;
  /** Path-alias map for the import pre-flight (from tsconfig paths). */
  aliases?: Record<string, string>;
  /** Skip the filesystem-bound import pre-flight (e.g. no tsconfig found). */
  skipImportCheck?: boolean;
  /** When true, validate + compute the plan but write nothing. */
  dryRun?: boolean;
}

export interface GenerateProviderModulesResult {
  providersDir: string;
  /** True when the providers dir is absent or holds no YAML — nothing to do. */
  skipped: boolean;
  /** Absolute paths of modules that were (or, in dryRun, would be) written. */
  written: string[];
  /** Per-file load/parse failures (schema errors), surfaced as issues too. */
  loadFailures: ProviderLoadError[];
  /** All blocking issues (load failures + cross-validation). Non-empty ⇒ no emit. */
  issues: AnalysisIssue[];
}

/**
 * Discover, validate, and emit provider modules. Validation is a **gate**: if
 * any provider fails to load or any cross-validation issue is found (including
 * the pre-flight import check), nothing is written and the issues are returned
 * for the caller to surface and fail `cdp gen` (RFC-0001 §1/§2 — fail before
 * emission, with a clear message, not at NestJS boot).
 */
export function generateProviderModules(
  opts: GenerateProviderModulesOptions,
): GenerateProviderModulesResult {
  const base: GenerateProviderModulesResult = {
    providersDir: opts.providersDir,
    skipped: false,
    written: [],
    loadFailures: [],
    issues: [],
  };

  if (!existsSync(opts.providersDir) || !statSync(opts.providersDir).isDirectory()) {
    return { ...base, skipped: true };
  }

  const files = findYamlFiles(opts.providersDir);
  if (files.length === 0) return { ...base, skipped: true };

  const { successes, failures } = loadProvidersFromYaml(files);
  const loaded: LoadedProvider[] = successes.map((s) => ({
    definition: s.definition,
    filePath: s.filePath,
  }));

  const issues: AnalysisIssue[] = failures.map((f) => ({
    severity: "error" as const,
    type: "provider_load_failed",
    message: `${f.error}${f.details?.length ? ` — ${f.details.join("; ")}` : ""}`,
    path: f.filePath,
  }));

  issues.push(
    ...validateProviders(loaded, {
      entitySurfaces: opts.entitySurfaces,
      sourceRoot: opts.sourceRoot,
      aliases: opts.aliases,
      skipImportCheck: opts.skipImportCheck,
    }),
  );

  // Gate: any blocking issue ⇒ emit nothing.
  if (issues.some((i) => i.severity === "error")) {
    return { ...base, loadFailures: failures, issues };
  }

  const written: string[] = [];
  for (const { definition, filePath } of loaded) {
    const sourceYaml = relativeSource(filePath);
    const content = generateProviderModule(definition, sourceYaml);
    const outPath = join(
      opts.outputRoot,
      definition.slug,
      `${definition.slug}.provider.module.ts`,
    );
    if (!opts.dryRun) {
      writeIfChanged(outPath, content);
    }
    written.push(outPath);
  }

  return { ...base, written, loadFailures: failures, issues };
}

/** Write only when content differs — avoids spurious mtime churn on re-emit. */
function writeIfChanged(outPath: string, content: string): void {
  if (existsSync(outPath) && readFileSync(outPath, "utf-8") === content) return;
  mkdirSync(dirname(outPath), { recursive: true });
  writeFileSync(outPath, content);
}

/**
 * Best-effort `definitions/providers/<file>` relative label for the banner.
 * Falls back to the basename if the path does not contain the conventional dir.
 */
function relativeSource(filePath: string): string {
  const marker = "definitions/providers/";
  const idx = filePath.lastIndexOf(marker);
  if (idx !== -1) return filePath.slice(idx);
  const slash = filePath.lastIndexOf("/");
  return slash === -1 ? filePath : `definitions/providers/${filePath.slice(slash + 1)}`;
}

