// Case helpers — local to avoid pulling .mjs into a TS module (mirrors the
// barrel-generator and event-codegen-generator convention).
// ---------------------------------------------------------------------------

function splitWords(str: string): string[] {
	return str
		.replace(/([a-z])([A-Z])/g, '$1 $2')
		.replace(/[_-]/g, ' ')
		.split(/\s+/)
		.filter((w) => w.length > 0)
		.map((w) => w.toLowerCase());
}

export function toKebabCase(str: string): string {
	return splitWords(str).join('-');
}

export function toPascalCase(str: string): string {
	return splitWords(str)
		.map((w) => w.charAt(0).toUpperCase() + w.slice(1))
		.join('');
}

export function toCamelCase(str: string): string {
	const words = splitWords(str);
	if (words.length === 0) return '';
	const [first, ...rest] = words;
	return (
		(first ?? '') + rest.map((w) => w.charAt(0).toUpperCase() + w.slice(1)).join('')
	);
}

export function toScreamingSnake(str: string): string {
	return splitWords(str).join('_').toUpperCase();
}

