export class OrchestrationEmissionError extends Error {
	override readonly name = 'OrchestrationEmissionError';
	constructor(
		public readonly issueType: string,
		public readonly patternName: string,
		message: string,
	) {
		super(`[${issueType}] ${patternName}: ${message}`);
	}
}

