interface RegistryNames {
	/** Token symbol identifier (e.g. `CRM_PORTS_REGISTRY`, `CRM_PORTS_AUTH_REGISTRY`). */
	tokenConst: string;
	/** Map type alias (e.g. `CrmPortsRegistryMap`, `CrmPortsAuthRegistryMap`). */
	mapType: string;
	/** Dispatcher method name (e.g. `select`, `selectAuth`). */
	method: string;
	/** ForRoot options field (e.g. `overrides`, `authOverrides`). */
	overrideField: string;
	/** Builder factory function name (`buildCrmPortsRegistryProviders`). */
	builderFn: string;
}

function patternNames(pattern: OrchestrationPatternDefinition) {
	const slug = toKebabCase(pattern.name);
	const pascal = toPascalCase(pattern.name);
	const screaming = toScreamingSnake(pattern.name);
	return { slug, pascal, screaming };
}

function registryNames(
	pattern: OrchestrationPatternDefinition,
	reg: OrchestrationRegistrySpec,
	isPrimary: boolean,
): RegistryNames {
	const { pascal, screaming } = patternNames(pattern);
	if (isPrimary) {
		return {
			tokenConst: `${screaming}_REGISTRY`,
			mapType: `${pascal}RegistryMap`,
			method: 'select',
			overrideField: 'overrides',
			builderFn: `build${pascal}RegistryProviders`,
		};
	}
	const namePascal = toPascalCase(reg.name!);
	const nameScreaming = toScreamingSnake(reg.name!);
	const nameCamel = toCamelCase(reg.name!);
	return {
		tokenConst: `${screaming}_${nameScreaming}_REGISTRY`,
		mapType: `${pascal}${namePascal}RegistryMap`,
		method: `select${namePascal}`,
		overrideField: `${nameCamel}Overrides`,
		builderFn: `build${pascal}RegistryProviders`,
	};
}

function dispatcherClassName(pattern: OrchestrationPatternDefinition): string {
	return pattern.dispatcher?.className ?? `${toPascalCase(pattern.name)}Dispatcher`;
}

function moduleClassName(pattern: OrchestrationPatternDefinition): string {
	return `${toPascalCase(pattern.name)}OrchestrationModule`;
}

function forRootOptionsTypeName(pattern: OrchestrationPatternDefinition): string {
	return `${toPascalCase(pattern.name)}ForRootOptions`;
}

// ---------------------------------------------------------------------------
// Import-grouping helpers (deterministic ordering)
// ---------------------------------------------------------------------------

interface TypeImport {
	specifier: string;
	name: string;
}

/** Group `import type { Name } from 'spec'` statements by specifier. */
function emitTypeImports(imports: TypeImport[]): string[] {
	const grouped = new Map<string, Set<string>>();
	for (const i of imports) {
		const set = grouped.get(i.specifier) ?? new Set();
		set.add(i.name);
		grouped.set(i.specifier, set);
	}
	const out: string[] = [];
	for (const specifier of [...grouped.keys()].sort()) {
		const names = [...grouped.get(specifier)!].sort();
		out.push(`import type { ${names.join(', ')} } from '${specifier}';`);
	}
	return out;
}

interface ValueImport {
	specifier: string;
	name: string;
}

/** Group `import { Name } from 'spec'` statements by specifier. */
function emitValueImports(imports: ValueImport[]): string[] {
	const grouped = new Map<string, Set<string>>();
	for (const i of imports) {
		const set = grouped.get(i.specifier) ?? new Set();
		set.add(i.name);
		grouped.set(i.specifier, set);
	}
	const out: string[] = [];
	for (const specifier of [...grouped.keys()].sort()) {
		const names = [...grouped.get(specifier)!].sort();
		out.push(`import { ${names.join(', ')} } from '${specifier}';`);
	}
	return out;
}

function allRegistries(
	pattern: OrchestrationPatternDefinition,
): Array<{ reg: OrchestrationRegistrySpec; names: RegistryNames; isPrimary: boolean }> {
	const list: Array<{ reg: OrchestrationRegistrySpec; names: RegistryNames; isPrimary: boolean }> = [];
	list.push({
		reg: pattern.registry,
		names: registryNames(pattern, pattern.registry, true),
		isPrimary: true,
	});
	for (const sibling of pattern.coKeyedRegistries ?? []) {
		list.push({
			reg: sibling,
			names: registryNames(pattern, sibling, false),
			isPrimary: false,
		});
	}
	return list;
}

