export function buildPatternFiles(
	pattern: OrchestrationPatternDefinition,
	outputRoot: string,
): OrchestrationPatternResult {
	assertEmittable(pattern);
	const slug = toKebabCase(pattern.name);
	const outputDir = path.join(outputRoot, slug);

	const make = (name: string, content: string): OrchestrationFileOutput => ({
		name,
		outputPath: path.join(outputDir, name),
		relativePath: path.join(slug, name),
		content,
	});

	const files: OrchestrationFileOutput[] = [
		make('tokens.ts', buildTokensTs(pattern)),
		make('registry.providers.ts', buildProvidersTs(pattern)),
		make('dispatcher.ts', buildDispatcherTs(pattern)),
		make('module.ts', buildModuleTs(pattern)),
		make('index.ts', buildIndexTs(pattern)),
	];

	return {
		patternName: pattern.name,
		slug,
		outputDir,
		files,
	};
}

export function generateOrchestrationModules(
	opts: OrchestrationGeneratorOptions,
): OrchestrationGeneratorResult {
	const { patterns, outputRoot, dryRun = false } = opts;
	const perPattern: OrchestrationPatternResult[] = [];
	const allFiles: OrchestrationFileOutput[] = [];

	for (const pattern of patterns) {
		const result = buildPatternFiles(pattern, outputRoot);
		perPattern.push(result);
		allFiles.push(...result.files);
	}

	// Top-level barrel (O-7).
	const rootBarrel: OrchestrationFileOutput = {
		name: 'index.ts',
		outputPath: path.join(outputRoot, 'index.ts'),
		relativePath: 'index.ts',
		content: buildRootBarrelTs(patterns),
	};
	allFiles.push(rootBarrel);

	let written = false;
	// When there are zero orchestration patterns, do NOT create the
	// `${orchestration_src}/` directory — consumers without patterns
	// shouldn't see a stray empty folder + index.ts after `entity new`.
	// The dry-run plan still includes the barrel for visibility.
	if (!dryRun && patterns.length > 0) {
		fs.mkdirSync(outputRoot, { recursive: true });
		for (const r of perPattern) {
			fs.mkdirSync(r.outputDir, { recursive: true });
			for (const f of r.files) {
				fs.writeFileSync(f.outputPath, f.content);
			}
		}
		fs.writeFileSync(rootBarrel.outputPath, rootBarrel.content);
		written = true;
	}

	return {
		outputRoot,
		patterns: perPattern,
		files: allFiles,
		written,
	};
}
