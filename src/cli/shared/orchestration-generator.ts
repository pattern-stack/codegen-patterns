/**
 * Orchestration generator (ADR-032 Phase 3-2 + 3-3).
 *
 * Pure-TS template-literal emitter — mirrors `event-codegen-generator.ts`
 * and `bridge-registry-generator.ts`. NO Hygen, NO EJS (operator gate
 * O-2). Pure content-builders are unit-testable in isolation; the single
 * orchestrating entrypoint handles disk I/O with `dryRun` semantics.
 *
 * Emits one directory per `OrchestrationPatternDefinition` under
 * `${paths.orchestration_src}/${slug}/`:
 *
 *   tokens.ts              — unique Symbol() per registry, typed map aliases
 *   registry.providers.ts  — buildXRegistryProviders() factory, frozen Map
 *   dispatcher.ts          — overload signatures + runtime select() impl
 *   module.ts              — DynamicModule with forRoot({ overrides, ... })
 *   index.ts               — barrel re-exports
 *
 * Co-keyed siblings (ADR-032 Phase 3-2 §1.1, O-1) are addressed by an
 * EXPLICIT `name:` field per sibling. The emitter uppercases that name
 * for token suffix and PascalCases it for method/option-key suffix. NO
 * auto-stripping of "I" prefix or "Strategy/Port/Adapter/Provider"
 * suffixes — authors pick the identifier that reads right.
 *
 * Risk flags (ADR-032 Phase 3-2 §6):
 *   R-1 — TS overload ordering: emit specific overloads first, then the
 *         widening signature, then the implementation signature LAST.
 *   R-3 — `Partial<Record<K, V>>` requires a literal-union `K`. Reject
 *         `keyType` of `string | number | any | unknown | symbol` at
 *         emission time.
 *   R-4 — Constructor-injected registry fields are `protected readonly`
 *         (NOT `private`) so subclasses can reach them per Decision 5.
 */

