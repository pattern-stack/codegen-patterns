export function buildTokensTs(pattern: OrchestrationPatternDefinition): string {
	const registries = allRegistries(pattern);

	const typeImports: TypeImport[] = [];
	for (const { reg } of registries) {
		typeImports.push({ specifier: reg.keyTypeImport!, name: reg.keyType });
		typeImports.push({ specifier: reg.valueTypeImport!, name: reg.valueType });
	}

	const lines: string[] = [];
	lines.push(HEADER.trimEnd());
	lines.push('');
	for (const i of emitTypeImports(typeImports)) lines.push(i);
	lines.push('');
	for (const { names } of registries) {
		lines.push(`export const ${names.tokenConst} = Symbol('${names.tokenConst}');`);
	}
	lines.push('');
	for (const { reg, names } of registries) {
		lines.push(
			`export type ${names.mapType} = Map<${reg.keyType}, ${reg.valueType}>;`,
		);
	}
	lines.push('');
	return lines.join('\n');
}

export function buildProvidersTs(pattern: OrchestrationPatternDefinition): string {
	const registries = allRegistries(pattern);

	// Provider class imports (concrete classes used by useFactory).
	const providerImports: ValueImport[] = [];
	for (const { reg } of registries) {
		for (const e of reg.entries) {
			providerImports.push({ specifier: e.providerImport!, name: e.provider });
		}
	}

	// Tokens + map types come from local ./tokens.js
	const tokenValueImports = registries.map(({ names }) => names.tokenConst);
	const mapTypeImports = registries.map(({ names }) => names.mapType);

	// Key-type + value-type imports — both used by `as` casts inside the
	// useFactory body and the new Map<K, V> annotation.
	const typeImports: TypeImport[] = [];
	for (const { reg } of registries) {
		typeImports.push({ specifier: reg.keyTypeImport!, name: reg.keyType });
		typeImports.push({ specifier: reg.valueTypeImport!, name: reg.valueType });
	}

	const optsType = forRootOptionsTypeName(pattern);

	const lines: string[] = [];
	lines.push(HEADER.trimEnd());
	lines.push('');
	lines.push(`import type { Provider } from '@nestjs/common';`);
	for (const i of emitTypeImports(typeImports)) lines.push(i);
	for (const i of emitValueImports(providerImports)) lines.push(i);
	// Local ./tokens import — combine values + type-only into one statement
	// in alphabetical order across both kinds. Use the standard split form:
	// `import { CONST, type MapType } from './tokens.js'`.
	const tokenLine = [
		...tokenValueImports.sort(),
		...mapTypeImports.sort().map((m) => `type ${m}`),
	].join(', ');
	lines.push(`import { ${tokenLine} } from './tokens.js';`);
	lines.push(`import type { ${optsType} } from './module.js';`);
	lines.push('');

	const { pascal } = patternNames(pattern);
	lines.push(
		`export function build${pascal}RegistryProviders(opts?: ${optsType}): Provider[] {`,
	);
	lines.push('\treturn [');
	for (const { reg, names } of registries) {
		const factoryArgs = reg.entries
			.map((e, i) => `${camelArg(e.provider, i)}: ${e.provider}`)
			.join(', ');
		lines.push('\t\t{');
		lines.push(`\t\t\tprovide: ${names.tokenConst},`);
		lines.push(`\t\t\tuseFactory: (${factoryArgs}) => {`);
		lines.push(`\t\t\t\tconst base: ${names.mapType} = new Map<${reg.keyType}, ${reg.valueType}>([`);
		for (const [i, e] of reg.entries.entries()) {
			lines.push(
				`\t\t\t\t\t['${e.key}' as ${reg.keyType}, ${camelArg(e.provider, i)}],`,
			);
		}
		lines.push('\t\t\t\t]);');
		lines.push(`\t\t\t\tif (opts?.${names.overrideField}) {`);
		lines.push(`\t\t\t\t\tfor (const [k, v] of Object.entries(opts.${names.overrideField})) {`);
		lines.push(`\t\t\t\t\t\tif (v !== undefined) base.set(k as ${reg.keyType}, v as ${reg.valueType});`);
		lines.push('\t\t\t\t\t}');
		lines.push('\t\t\t\t}');
		lines.push(`\t\t\t\treturn base as ${names.mapType};`);
		lines.push('\t\t\t},');
		const injectList = reg.entries.map((e) => e.provider).join(', ');
		lines.push(`\t\t\tinject: [${injectList}],`);
		lines.push('\t\t},');
	}
	lines.push('\t];');
	lines.push('}');
	lines.push('');
	return lines.join('\n');
}

/** Stable per-entry factory argument name. */
function camelArg(provider: string, index: number): string {
	const camel = provider.charAt(0).toLowerCase() + provider.slice(1);
	// Disambiguate if two entries (across registries) share a class name.
	return `${camel}_${index}`;
}

export function buildDispatcherTs(pattern: OrchestrationPatternDefinition): string {
	const registries = allRegistries(pattern);
	const className = dispatcherClassName(pattern);
	const errorClass = `${className}Error`;

	// Imports
	const typeImports: TypeImport[] = [];
	for (const { reg } of registries) {
		typeImports.push({ specifier: reg.keyTypeImport!, name: reg.keyType });
		typeImports.push({ specifier: reg.valueTypeImport!, name: reg.valueType });
	}
	const providerImports: ValueImport[] = [];
	for (const { reg } of registries) {
		for (const e of reg.entries) {
			providerImports.push({ specifier: e.providerImport!, name: e.provider });
		}
	}
	const tokenValues = registries.map(({ names }) => names.tokenConst);
	const mapTypes = registries.map(({ names }) => names.mapType);

	const lines: string[] = [];
	lines.push(HEADER.trimEnd());
	lines.push('');
	lines.push(`import { Inject, Injectable } from '@nestjs/common';`);
	for (const i of emitTypeImports(typeImports)) lines.push(i);
	for (const i of emitValueImports(providerImports)) lines.push(i);
	const tokenLine = [
		...tokenValues.sort(),
		...mapTypes.sort().map((m) => `type ${m}`),
	].join(', ');
	lines.push(`import { ${tokenLine} } from './tokens.js';`);
	lines.push('');
	lines.push(`export class ${errorClass} extends Error {}`);
	lines.push('');
	lines.push('@Injectable()');
	lines.push(`export class ${className} {`);
	// Constructor — protected readonly registry fields (R-4)
	lines.push('\tconstructor(');
	for (const { names } of registries) {
		const fieldName = names.method === 'select' ? 'registry' : `${camelFromMethod(names.method)}Registry`;
		lines.push(
			`\t\t@Inject(${names.tokenConst}) protected readonly ${fieldName}: ${names.mapType},`,
		);
	}
	lines.push('\t) {}');
	lines.push('');

	// Per-registry select method (R-1: specific overloads first, then
	// widening signature, then implementation signature, then body).
	for (const { reg, names } of registries) {
		const fieldName =
			names.method === 'select' ? 'registry' : `${camelFromMethod(names.method)}Registry`;
		// Specific overloads
		for (const e of reg.entries) {
			lines.push(`\t${names.method}(key: '${e.key}'): ${e.provider};`);
		}
		// Widening overload
		lines.push(`\t${names.method}(key: ${reg.keyType}): ${reg.valueType};`);
		// Implementation signature
		lines.push(`\t${names.method}(key: ${reg.keyType}): ${reg.valueType} {`);
		lines.push(`\t\tconst entry = this.${fieldName}.get(key);`);
		lines.push(
			`\t\tif (!entry) throw new ${errorClass}(\`Unknown ${reg.keyType}: \${String(key)}\`);`,
		);
		lines.push(`\t\treturn entry;`);
		lines.push('\t}');
		lines.push('');
	}

	// Decision 5 documentation comment for assemblySlot.
	if (pattern.dispatcher?.assemblySlot) {
		lines.push(
			`\t// ADR-032 Decision 5 — \`assemblySlot: '${pattern.dispatcher.assemblySlot}'\` is a`,
		);
		lines.push(
			`\t// subclass contract, not emitted here. Consumers subclass and add the`,
		);
		lines.push(
			`\t// \`${pattern.dispatcher.assemblySlot}(...)\` method themselves.`,
		);
	}
	lines.push('}');
	lines.push('');
	return lines.join('\n');
}

function camelFromMethod(method: string): string {
	// `selectAuth` -> `auth`
	if (method.startsWith('select') && method.length > 'select'.length) {
		const tail = method.slice('select'.length);
		return tail.charAt(0).toLowerCase() + tail.slice(1);
	}
	return method;
}

export function buildModuleTs(pattern: OrchestrationPatternDefinition): string {
	const registries = allRegistries(pattern);
	const className = dispatcherClassName(pattern);
	const moduleName = moduleClassName(pattern);
	const optsType = forRootOptionsTypeName(pattern);
	const { pascal } = patternNames(pattern);

	const typeImports: TypeImport[] = [];
	for (const { reg } of registries) {
		typeImports.push({ specifier: reg.keyTypeImport!, name: reg.keyType });
		typeImports.push({ specifier: reg.valueTypeImport!, name: reg.valueType });
	}
	const tokenValues = registries.map(({ names }) => names.tokenConst);

	const lines: string[] = [];
	lines.push(HEADER.trimEnd());
	lines.push('');
	lines.push(`import { type DynamicModule, Module } from '@nestjs/common';`);
	for (const i of emitTypeImports(typeImports)) lines.push(i);
	lines.push(`import { ${className} } from './dispatcher.js';`);
	lines.push(`import { build${pascal}RegistryProviders } from './registry.providers.js';`);
	lines.push(`import { ${tokenValues.sort().join(', ')} } from './tokens.js';`);
	lines.push('');

	lines.push(`export interface ${optsType} {`);
	for (const { reg, names } of registries) {
		lines.push(
			`\t${names.overrideField}?: Partial<Record<${reg.keyType}, ${reg.valueType}>>;`,
		);
	}
	lines.push('}');
	lines.push('');
	lines.push('@Module({})');
	lines.push(`export class ${moduleName} {`);
	lines.push(`\tstatic forRoot(opts?: ${optsType}): DynamicModule {`);
	lines.push('\t\treturn {');
	lines.push(`\t\t\tmodule: ${moduleName},`);
	lines.push(
		`\t\t\tproviders: [...build${pascal}RegistryProviders(opts), ${className}],`,
	);
	const exportItems = [...tokenValues.sort(), className];
	lines.push(`\t\t\texports: [${exportItems.join(', ')}],`);
	lines.push('\t\t};');
	lines.push('\t}');
	lines.push('}');
	lines.push('');
	return lines.join('\n');
}

export function buildIndexTs(pattern: OrchestrationPatternDefinition): string {
	const lines: string[] = [];
	lines.push(HEADER.trimEnd());
	lines.push('');
	lines.push(`export * from './tokens.js';`);
	lines.push(`export * from './dispatcher.js';`);
	lines.push(`export * from './module.js';`);
	lines.push(`export * from './registry.providers.js';`);
	lines.push('');
	return lines.join('\n');
}

// ---------------------------------------------------------------------------
// Top-level barrel (O-7) — `${orchestration_src}/index.ts` re-exports every
// pattern's per-pattern barrel.
// ---------------------------------------------------------------------------

export function buildRootBarrelTs(
	patterns: ReadonlyArray<OrchestrationPatternDefinition>,
): string {
	const lines: string[] = [];
	lines.push(HEADER.trimEnd());
	lines.push('');
	if (patterns.length === 0) {
		lines.push('// No orchestration patterns registered.');
		lines.push('export {};');
		lines.push('');
		return lines.join('\n');
	}
	const slugs = patterns.map((p) => toKebabCase(p.name)).sort();
	for (const slug of slugs) {
		lines.push(`export * from './${slug}/index.js';`);
	}
	lines.push('');
	return lines.join('\n');
}

