const FORBIDDEN_KEY_TYPES = new Set([
	'string',
	'number',
	'symbol',
	'any',
	'unknown',
]);

function assertEmittable(pattern: OrchestrationPatternDefinition): void {
	const allRegistries: OrchestrationRegistrySpec[] = [
		pattern.registry,
		...(pattern.coKeyedRegistries ?? []),
	];

	// Co-keyed siblings MUST carry an explicit `name` (O-1).
	for (const sibling of pattern.coKeyedRegistries ?? []) {
		if (typeof sibling.name !== 'string' || sibling.name.length === 0) {
			throw new OrchestrationEmissionError(
				'pattern_cokeyed_missing_name',
				pattern.name,
				`Co-keyed registry (valueType '${sibling.valueType}') is missing the ` +
					`required 'name' field. Each co-keyed sibling must declare an ` +
					`explicit name; the emitter uppercases it for the token suffix and ` +
					`PascalCases it for the dispatcher method (e.g. name: 'auth' ⇒ ` +
					`'\${PATTERN_CONST}_AUTH_REGISTRY' + 'selectAuth(...)'). ADR-032 O-1.`,
			);
		}
	}

	for (const reg of allRegistries) {
		// R-3: Partial<Record<K, V>> requires a literal-union K. Reject the
		// primitive-type keyTypes that would silently accept any string at
		// runtime + collapse the per-key narrowing in the dispatcher.
		if (FORBIDDEN_KEY_TYPES.has(reg.keyType)) {
			throw new OrchestrationEmissionError(
				'pattern_keytype_unresolved',
				pattern.name,
				`registry keyType '${reg.keyType}' is a primitive — Partial<Record<K, V>> ` +
					`requires a literal-union K, and per-overload narrowing requires a ` +
					`closed enum/union. Use a string-literal union or a TypeScript enum.`,
			);
		}

		// O-3 / O-8: import-path fields must be present at emission time.
		if (typeof reg.keyTypeImport !== 'string' || reg.keyTypeImport.length === 0) {
			throw new OrchestrationEmissionError(
				'pattern_missing_import_path',
				pattern.name,
				`registry (keyType '${reg.keyType}') is missing 'keyTypeImport'. ` +
					`Phase 3-2 emission requires every keyType to carry a module specifier.`,
			);
		}
		if (typeof reg.valueTypeImport !== 'string' || reg.valueTypeImport.length === 0) {
			throw new OrchestrationEmissionError(
				'pattern_missing_import_path',
				pattern.name,
				`registry (valueType '${reg.valueType}') is missing 'valueTypeImport'.`,
			);
		}
		for (const e of reg.entries) {
			if (typeof e.providerImport !== 'string' || e.providerImport.length === 0) {
				throw new OrchestrationEmissionError(
					'pattern_missing_import_path',
					pattern.name,
					`entry '${e.key}' provider '${e.provider}' is missing 'providerImport'.`,
				);
			}
		}
	}
}

