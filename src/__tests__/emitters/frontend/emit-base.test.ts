/**
 * Frontend emitter — base files tests (ADR-038, FE-2).
 *
 * query-client.ts (staleTime/gcTime, replaceability comment) and config.ts
 * (per-entity mode table, getSyncMode/setEntityConfig, no 'offline').
 */

import { describe, expect, it } from 'bun:test';
import {
	buildConfigFile,
	buildQueryClientFile,
} from '../../../emitters/frontend/emit-base';
import { ctx, entry } from './_helpers';

describe('emit-base — query-client.ts', () => {
	it('exports a QueryClient with staleTime 60s and gcTime 5m', () => {
		const out = buildQueryClientFile();
		expect(out).toContain("import { QueryClient } from '@tanstack/react-query';");
		expect(out).toContain('export const queryClient = new QueryClient({');
		expect(out).toContain('staleTime: 60 * 1000');
		expect(out).toContain('gcTime: 5 * 60 * 1000');
	});

	it('carries the replaceability comment and the banner', () => {
		const out = buildQueryClientFile();
		expect(out).toContain('Replaceable');
		expect(out.startsWith('// @generated by @pattern-stack/codegen')).toBe(true);
	});
});

describe('emit-base — config.ts', () => {
	it('builds the per-entity mode table from resolved modes', () => {
		const c = ctx(
			[entry('contact', 'contacts', 'api'), entry('account', 'accounts')],
			{ globalSyncMode: 'electric' },
		);
		const out = buildConfigFile(c);
		expect(out).toContain("contact: { mode: 'api' },");
		expect(out).toContain("account: { mode: 'electric' },");
	});

	it('emits the EntityName union sorted by name', () => {
		const c = ctx([entry('zeta', 'zetas'), entry('alpha', 'alphas')]);
		const out = buildConfigFile(c);
		expect(out).toContain("export type EntityName = 'alpha' | 'zeta';");
	});

	it("declares SyncMode as 'api' | 'electric' with no 'offline' as a mode value", () => {
		const c = ctx([entry('contact', 'contacts')]);
		const out = buildConfigFile(c);
		expect(out).toContain("export type SyncMode = 'api' | 'electric';");
		// 'offline' must never appear as a quoted mode literal (the prose
		// deferral comment that references it is fine — it points at OQ-6).
		expect(out).not.toContain("'offline'");
		expect(out).not.toContain('| offline');
	});

	it('exports getSyncMode and setEntityConfig runtime accessors', () => {
		const c = ctx([entry('contact', 'contacts')]);
		const out = buildConfigFile(c);
		expect(out).toContain('export function getSyncMode(entity: EntityName): SyncMode {');
		expect(out).toContain('export function setEntityConfig(entity: EntityName, config: EntitySyncConfig): void {');
		expect(out).toContain('export const defaultConfig:');
	});

	it('points the offline-deferral comment at the parent spec', () => {
		const out = buildConfigFile(ctx([entry('contact', 'contacts')]));
		expect(out).toContain('2026-06-04-frontend-pipeline-rebuild.md OQ-6');
	});

	it('is deterministic for the same context', () => {
		const make = () =>
			ctx([entry('contact', 'contacts', 'api'), entry('account', 'accounts')]);
		expect(buildConfigFile(make())).toBe(buildConfigFile(make()));
	});
});
